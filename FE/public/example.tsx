import React, { useState } from 'react'
import ImageUploading from 'react-images-uploading';

const Profile = () => {
  const [chooseImages, setChooseImages] = useState([]);
  const maxNumber = 2;
  
const onChange = (imageList, addUpdateIndex) => {
  // data for submit
  console.log("Thêm ảnh thành công",imageList, addUpdateIndex);
  setChooseImages(imageList);
};
  return  (
    <>
      <ImageUploading
      /* multiple */
      maxNumber={maxNumber}
      value={chooseImages}
      onChange={onChange}
      dataURLKey="data_url">
      {({
        imageList,
        onImageUpload,
        onImageRemoveAll,
        onImageUpdate,
        onImageRemove,
        /* isDragging,
        dragProps, */
      }) => (
        // write your building UI
        <div className="upload__image-wrapper">
          <button
/*             style={isDragging ? { color: 'red' } : undefined} */
            onClick={onImageUpload}
            /* {...dragProps} */
            
          >
            Thêm ảnh
          </button>
          &nbsp;
          <button onClick={onImageRemoveAll}>Xóa tất cả ảnh</button>
          {imageList.map((image, index) => (
            <div key={index} >
              <img src={image['data_url']} alt="" width="270px" />
              <Location></Location>
              <div >
                <button onClick={() => onImageUpdate(index)}>Thay đổi ảnh</button>
                <button onClick={() => onImageRemove(index)}>Xóa ảnh</button>
              </div>
            </div>
          ))}
        </div>
      )}
    </ImageUploading>
    </>
  )
}
export default Profile
